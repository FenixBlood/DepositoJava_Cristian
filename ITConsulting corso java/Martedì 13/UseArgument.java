public class UseArgument {
    public static void main(String[] args) {
        // qui si dichiara una variabile 
        String pippo= "pippo";
        // Questi comandi stampano quello che hanno dentro le loro parentesi (Si potrebbero concatenare tutte in una)
        System.out.print("Hi, ");
        System.out.println(pippo);
        System.out.println(". How are you?");
    }
}
